public class AddNumbers 
{
    public static void main(String[] args) {
        // Step 1: Initialize 2 integers
        int x = 10;
        int y = 20;

        // Step 2: Initialize z as the sum of x and y
        int z = x + y;

        // Step 3: Display the value of z
        System.out.println("The sum is: " + z);
    }
}
